
import java.util.Scanner;
 
public class LcmGcf {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t-- > 0) {
            int n = sc.nextInt();
            System.out.print(n - 3 + " " + 1 + " " + 1 + " " + 1);
            System.out.println();
        }
    }
}